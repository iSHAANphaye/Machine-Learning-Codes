import java.util.Scanner;
class precision
{
    public static void main(String args[])
    {
        System.out.printf("%.1f\n",12.37);
        System.out.printf("%6d\n",12345);      //width of 6 and is right justified
        System.out.printf("%6.2f\n",123.45);   //Decimal is included in a space
    }
}